public class review2 {

	public static void main(String[] args) {
		// 전혀 모르겠음. 
		for(int j = 0;j < 10;j++) {
			for(int k = 10; k > 0; k--)
			{
				if(k <= j)
					System.out.printf(" ");
				else
				System.out.printf("*");
			}System.out.println("");
			
		}
	}
}
